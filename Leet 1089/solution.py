# Duplicate zeros
#
# Example
#
# input: [0,1,0,2,3,0,4,5,0]
# ----------
# [0, 1, 0, 2, 3, 0, 4, 5, 5]
# [0, 1, 0, 2, 3, 0, 4, 4, 5]
# [0, 1, 0, 2, 3, 0, 0, 4, 5]
# ----------
# [0, 1, 0, 2, 3, 0, 0, 4, 4]
# [0, 1, 0, 2, 3, 0, 0, 0, 4]
# [0, 1, 0, 2, 3, 0, 0, 0, 4]
# [0, 1, 0, 2, 3, 3, 0, 0, 4]
# [0, 1, 0, 2, 2, 3, 0, 0, 4]
# [0, 1, 0, 0, 2, 3, 0, 0, 4]
# ----------
# [0, 1, 0, 0, 2, 3, 0, 0, 0]
# [0, 1, 0, 0, 2, 3, 0, 0, 0]
# [0, 1, 0, 0, 2, 3, 3, 0, 0]
# [0, 1, 0, 0, 2, 2, 3, 0, 0]
# [0, 1, 0, 0, 0, 2, 3, 0, 0]
# [0, 1, 0, 0, 0, 2, 3, 0, 0]
# [0, 1, 1, 0, 0, 2, 3, 0, 0]
# [0, 0, 1, 0, 0, 2, 3, 0, 0]
# ----------
# [0, 0, 1, 0, 0, 2, 3, 0, 0]
# ----------


def duplicate_zeros(nums: list[int]) -> None:
    """ 
    """
    n: int = len(nums)
    
    for i in reversed(range(n - 1)):

        if nums[i] == 0:
            for j in reversed(range(i, n - 1)):
                nums[j + 1] = nums[j]


def main() -> None:
    nums: list[int] = [0, 1, 0, 2, 3, 0, 4, 5, 0]
    duplicate_zeros(nums)
    print(nums)


if __name__ == "__main__":
    main()