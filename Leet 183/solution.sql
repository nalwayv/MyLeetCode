-- 183. Customers Who Never Order
--
-- Customers table:
-- +----+-------+
-- | id | name  |
-- +----+-------+
-- | 1  | Joe   |
-- | 2  | Henry |
-- | 3  | Sam   |
-- | 4  | Max   |
-- +----+-------+
--
-- Orders table:
-- +----+------------+
-- | id | customerId |
-- +----+------------+
-- | 1  | 3          |
-- | 2  | 1          |
-- +----+------------+

SELECT Customers.name AS 'Customers'
FROM Customers
-- filter
WHERE NOT EXISTS(
	SELECT * FROM Orders WHERE Customers.id = Orders.customerId)